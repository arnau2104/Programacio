package Exercici9_6;

public class Ciutat {

	private String Ciutat;

	
	public Ciutat() {
		
	}
	
	public Ciutat( String Ciutat) {
		this.Ciutat = Ciutat;
		
	}
	
	public String toString() {
		return "Ciutat: " + Ciutat;
	}

	public String getCiutat() {
		return Ciutat;
	}
	
	
}
